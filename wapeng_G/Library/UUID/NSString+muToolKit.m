#import "NSString+muToolKit.h"

@implementation NSString (muToolKit)

-(NSString*)lastChar
{
    int len = [self length];
    NSString* last = [self substringFromIndex:len-1];
    return last;
}

-(NSString*)removeLastChar
{
    int len = [self length];
    NSString* after =[self substringToIndex:len-1];
    return after;
}
@end
